import { Box, Button, Paper, Typography } from '@mui/material'
import { useEffect} from 'react';
import { useActivities } from '../../../lib/hooks/useActivities';
import { useNavigate, useParams } from 'react-router';
import {useForm, type FieldValues} from 'react-hook-form';
import { activitySchema, type ActivitySchema } from '../../../lib/schemas/activitySchema';
import {zodResolver} from '@hookform/resolvers/zod';
import TextInput from '../../../app/shared/components/TextInput';
import SelectInput from '../../../app/shared/components/SelectInput';
import { categoryOptions } from './categoryOptions';
import DateTimeInput from '../../../app/shared/components/DateTimeInput';
import LocationInput from '../../../app/shared/components/LocationInput';

export default function ActivityForm() {

  const  { reset, control, handleSubmit } = useForm<ActivitySchema>({
    mode: 'onTouched',
    resolver: zodResolver(activitySchema)
  });
  const {id} = useParams();
  const { updateActivity, createActivity, activity, isLoadingActivity } = useActivities(id);
  const navigate = useNavigate();

  useEffect(() => {
    if(activity) reset({
      ...activity,
      location: {
        city: activity.city,
        venue: activity.venue,
        latitude: activity.latitude,
        longitude: activity.longitude
      }
    });
  },[activity, reset]);

  const onSubmit = async (data: FieldValues) => {
    const {location, ...rest} = data;

    const flattenedData = {...rest, ...location};

    try {
      if(activity){
        updateActivity.mutate({...activity, ...flattenedData}, {
          onSuccess: () => navigate(`/activities/${activity.id}`)
        })
      }
      else{
        createActivity.mutate(flattenedData, {
          onSuccess: (id) => navigate(`/activities/${id}`)
        })
      }
    } catch (error) {
      console.log(error);
    }
  };

  if(isLoadingActivity) return <Typography>Loading activity...</Typography>

  return (
    <Paper sx={{borderRadius: 3, padding: 3}}>
        <Typography variant='h5' gutterBottom color='primary'>{activity ? 'Editing' : 'Create'} Activity</Typography>
        <Box component="form" onSubmit={handleSubmit(onSubmit)} display="flex" flexDirection="column" gap={3}>
            <TextInput label="Title" name='title' control={control} />
            <TextInput label="Description" name='description' control={control} multiline rows={3} />
            <Box display='flex' gap={3}>
                <SelectInput items={categoryOptions} label="Category" name='category' control={control} />
                <DateTimeInput label="Date" name='date' control={control} />
            </Box>
            <LocationInput label='Enter the location' name='location' control={control}></LocationInput>
            <Box display='flex' justifyContent='end' gap={3}>
                <Button onClick={() => navigate(-1)} color='inherit'>Cancel</Button>
                <Button type='submit' color='success' variant='contained' disabled={updateActivity.isPending || createActivity.isPending}>Submit</Button>
            </Box>
        </Box>
    </Paper>
  )
}
